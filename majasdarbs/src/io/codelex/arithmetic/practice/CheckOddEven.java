package io.codelex.arithmetic.practice;

public class CheckOddEven {
    public static void main(String[] args) {

        int num = 1;

        if (num % 2 == 0) {
            System.out.println("Even Number");
        } else {
            System.out.println("Odd Number");
        }
        System.out.println("bye!");
    }
}
